### 👺 cbind 或 rbind 后面补的向量长度要分别等于矩阵的行数或列数，过长就会截断、过短会从头重复直到补齐。如果不是整数倍就会有警告。

cbind(1, c(1,-1,10)) ;
##      [,1] [,2]
## [1,]    1    1
## [2,]    1   -1
## [3,]    1   10

rbind(1, c(1,-1,10)) ;
##      [,1] [,2] [,3]
## [1,]    1    1    1
## [2,]    1   -1   10

